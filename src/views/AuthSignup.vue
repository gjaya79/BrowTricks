<template>
  <div class="flex flex-col items-center text-left w-full">
    <div class="px-4 max-w-md">
      <img
        src="https://res.cloudinary.com/whynotearth/image/upload/v1600874347/BrowTricks/static_v2/image-login_crwfva.png"
        alt=""
      />
    </div>
    <div
      class="flex-grow w-full bg-surface rounded-t-xl px-4 py-8 flex flex-col items-center"
    >
      <form
        @submit.prevent="submit"
        class="flex flex-col w-full max-w-sm flex-grow justify-between"
      >
        <div class="">
          <MaterialInput
            v-model.trim="$v.firstName.$model"
            label="First Name"
            :attrs="{ autocomplete: 'first-name' }"
            :error="$v.firstName.$error"
          >
            <p v-if="!$v.firstName.required">
              First Name is required
            </p>
          </MaterialInput>
          <MaterialInput
            v-model.trim="$v.lastName.$model"
            label="Last Name"
            :attrs="{ autocomplete: 'last-name' }"
            :error="$v.lastName.$error"
          >
            <p v-if="!$v.lastName.required">
              Last Name is required
            </p>
          </MaterialInput>
          <MaterialInput
            v-model.trim="$v.phoneNumber.$model"
            label="Phone Number"
            type="tel"
            :attrs="{ autocomplete: 'tel', inputmode: 'tel' }"
            :error="$v.phoneNumber.$error"
          >
            <p v-if="!$v.phoneNumber.required">
              Phone number is required
            </p>
            <p v-else-if="!$v.phoneNumber.isPhoneNumberValid">
              Please enter a valid phone number
            </p>
          </MaterialInput>
          <MaterialInput
            type="email"
            v-model.trim="$v.email.$model"
            label="Email Address"
            :attrs="{ autocomplete: 'email', inputmode: 'email' }"
            :error="$v.email.$error"
          >
            <p v-if="!$v.email.required">
              Email is required
            </p>
            <p v-else-if="!$v.email.email">
              Please enter an email address
            </p>
          </MaterialInput>
          <MaterialInput
            type="password"
            v-model.trim="$v.password.$model"
            label="Password"
            :attrs="{ autocomplete: 'new-password' }"
            :error="$v.password.$error"
          >
            <p v-if="!$v.password.required">
              Password is required
            </p>
          </MaterialInput>
        </div>

        <p v-if="errorMessage" class="mb-4 text-error tg-body-mobile">
          {{ errorMessage }}
        </p>

        <div>
          <Button type="submit" title="Let's Get Started" />
          <p class="mt-4 tg-body-mobile text-center">
            Already have an account?
            <router-link :to="{ name: 'AuthLogin' }" class="text-primary-blue">
              Login
            </router-link>
          </p>
        </div>
      </form>
    </div>
  </div>
</template>

<script>
import MaterialInput from '@/components/inputs/MaterialInput.vue';
import { required, email } from 'vuelidate/lib/validators';
import { mapActions } from 'vuex';
import { showOverlayAndRedirect, isPhoneNumberValid } from '@/helpers';
import { get } from 'lodash-es';

export default {
  name: 'AuthSignup',
  components: {
    MaterialInput
  },
  data() {
    return {
      errorMessage: '',
      firstName: '',
      lastName: '',
      email: '',
      password: '',
      phoneNumber: ''
    };
  },
  validations: {
    firstName: {
      required
    },
    lastName: {
      required
    },
    email: {
      required,
      email
    },
    password: {
      required
    },
    phoneNumber: {
      required,
      isPhoneNumberValid
    }
  },
  created() {
    this.clearError();
  },
  methods: {
    ...mapActions('auth', ['register']),
    clearError() {
      this.errorMessage = '';
    },
    submit() {
      this.clearError();

      this.$v.$touch();
      if (this.$v.$invalid) {
        return;
      }

      this.register({
        params: {
          body: {
            password: this.password,
            email: this.email,
            firstName: this.firstName,
            lastName: this.lastName,
            phoneNumber: this.phoneNumber
          }
        }
      })
        .then(this.onSuccess)
        .catch(error => {
          this.errorMessage = get(
            error,
            'response.data[0].description',
            'Something went wrong'
          );
          console.log(error, error.response);
        });
    },
    onSuccess() {
      showOverlayAndRedirect({
        title: 'Success!',
        route: { name: 'AuthNumberVerify' }
      });
    }
  }
};
</script>
